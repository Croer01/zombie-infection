class Scene {
    static const int CELLS_PER_ROW = 32;
    static const int CELLS_PER_COLUMN = 32;
    static const int CELL_SIZE = 10;
    static const byte ENEMIES_LENGTH = 5;
    Player player;
    Enemy enemy[ENEMIES_LENGTH];

  public:
    Scene() {
      Camera::getInstance()->setBounds(CELLS_PER_ROW * CELL_SIZE, CELLS_PER_COLUMN * CELL_SIZE);
      for (int i = 0; i < ENEMIES_LENGTH; i++) {
        enemy[i].respawn(20 + i * 20, 20 + i * 20);
      }
    }

    void update() {
      player.update();

      for (int i = 0; i < ENEMIES_LENGTH; i++) {
        enemy[i].update(&player);
      }
    };

    void render() {
      for (int i = 0; i < CELLS_PER_ROW; i++) {
        for (int j = 0; j < CELLS_PER_COLUMN; j++) {
          int index = i * CELLS_PER_ROW + j;

          if (pgm_read_byte_near(sceneLayout + index) == 0x01) {
            arduboy.fillRect(Camera::getInstance()->getX() + i * CELL_SIZE, Camera::getInstance()->getY() + j * CELL_SIZE, CELL_SIZE, CELL_SIZE, WHITE);
          }
        }
      }
      player.render();
      for (int i = 0; i < ENEMIES_LENGTH; i++) {
        enemy[i].render();
      }
    };
};

